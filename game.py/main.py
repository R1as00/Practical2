class Warrior:
    def __init__(self, name):
        self.name = name
        self.health = 100
        self.damage = 20

    def attacks(self, enemy):
        print(f"Боец {self.name} атакует бойца {enemy.name}")
        enemy.health -= self.damage
        print(f"Боец {self.name} наносит {self.damage} урона")
        print(f"Здоровье бойца {enemy.name}: {enemy.health}")
        time.sleep(2)  # задержка в 2 секунды


p1 = Warrior("Руруни кенши")
p2 = Warrior("Миямото Мусаси")

while True:
    if p1.health <= 0:
        print(f"Боец {p2.name} победил")
        break
    elif p2.health <= 0:
        print(f"Боец {p1.name} победил")
        break

    if randint(0, 100) <= 50:
        p1.attacks(p2)
    else:
        p2.attacks(p1)
